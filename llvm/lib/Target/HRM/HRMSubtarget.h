#ifndef LLVM_HRM_SUBTARGET_H
#define LLVM_HRM_SUBTARGET_H

#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#include "HRMFrameLowering.h"
#include "HRMISelLowering.h"
#include "HRMInstrInfo.h"
// #include "HRMSelectionDAGInfo.h"
#include "MCTargetDesc/HRMMCTargetDesc.h"

#define GET_SUBTARGETINFO_HEADER
#include "HRMGenSubtargetInfo.inc"

namespace llvm {

/// A specific HRM target MCU.
class HRMSubtarget : public HRMGenSubtargetInfo {
public:
  //! Creates an HRM subtarget.
  //! \param TT  The target triple.
  //! \param CPU The CPU to target.
  //! \param FS  The feature string.
  //! \param TM  The target machine.
  HRMSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
               const HRMTargetMachine &TM);

  const HRMInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const HRMTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const HRMSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
  const HRMRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  /// Parses a subtarget feature string, setting appropriate options.
  /// \note Definition of function is auto generated by `tblgen`.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  HRMSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS,
                                                const TargetMachine &TM);

  // Subtarget feature getters.
#define GET_SUBTARGETINFO_MACRO(ATTRIBUTE, DEFAULT, GETTER)                    \
  bool GETTER() const { return ATTRIBUTE; }
#include "HRMGenSubtargetInfo.inc"

 

private:
  /// The ELF e_flags architecture.
  unsigned ELFArch = 0;

  // Subtarget feature settings
#define GET_SUBTARGETINFO_MACRO(ATTRIBUTE, DEFAULT, GETTER)                    \
  bool ATTRIBUTE = DEFAULT;
#include "HRMGenSubtargetInfo.inc"

  HRMInstrInfo InstrInfo;
  HRMFrameLowering FrameLowering;
  HRMTargetLowering TLInfo;
  HRMSelectionDAGInfo TSInfo;
};

} // end namespace llvm

#endif // LLVM_HRM_SUBTARGET_H
